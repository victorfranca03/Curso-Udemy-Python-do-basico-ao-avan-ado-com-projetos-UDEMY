'''
     slice é usada em Python para extrair partes específicas de uma sequência, 
    como uma lista, uma string ou uma tupla. 
    Ela permite especificar um intervalo de índices para selecionar os elementos desejados, 
    retornando uma nova sequência com esses elementos. O formato básico é `sequencia[início:fim]`,
    onde `início` é o índice inicial (inclusive) e `fim` é o índice final (exclusive).

'''

# Exemplo simples usando o slice 

valor = 99.75
valor = str(valor)

# o index sempre começa pelo 0 dando continuidade 1234...

print(valor[3:5])

# Resultado do print 75 que pega do indice 3 com inicio e o 5 no final